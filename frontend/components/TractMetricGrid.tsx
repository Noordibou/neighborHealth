export function TractMetricGrid({ tract }: { tract: TractDetail }) {
  const byName = new Map(tract.indicators.map((i) => [i.metric_name, i]));

  return (
    <div className="grid gap-4 sm:grid-cols-2 lg:grid-cols-3">
      {METRIC_KEYS.map((key) => {
        const ind = byName.get(key);
        return (
          <MetricCard
            key={key}
            title={METRIC_LABELS[key]}
            valueLabel={formatMetricValue(key, ind?.value ?? null)}
            statePct={ind?.percentile_state ?? null}
            nationalPct={ind?.percentile_national ?? null}
            icon={iconFor(key)}
          />
        );
      })}
    </div>
  );
}
